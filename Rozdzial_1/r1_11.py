# przykłady dla pętli for - funkcja range()

for i in range(3):
    print(f"Napis po raz {i}")

print("==========")
for i in range(1, 5):
    print(f"Napis po raz {i}")

print("==========")
for i in range(1, 15, 3):
    print(f"Napis po raz {i}")

print("==========")
for i in range(15, 5, -3):
    print(f"Napis po raz {i}")

print("==========")
for i in range(-3, 3):
    print(f"Napis po raz {i}")
